<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import { defineProps, defineEmits } from 'vue';

/*--------------------------------------------------------------------------------------------------------------------*/

const emit = defineEmits(['update:modelValue']);

/*--------------------------------------------------------------------------------------------------------------------*/

defineProps({
    modelValue: String
});

/*--------------------------------------------------------------------------------------------------------------------*/

const updateValue = (e) => {

    emit('update:modelValue', e.target.value);
};

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->

    <div v-bind="$attrs">

        <input type="text" :value="modelValue" @input="updateValue" />

    </div>

    <!-- *********************************************************************************************************** -->

</template>
