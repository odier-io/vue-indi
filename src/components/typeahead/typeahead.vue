<script setup>
/*--------------------------------------------------------------------------------------------------------------------*/

import { defineProps, defineEmits } from 'vue';

/*--------------------------------------------------------------------------------------------------------------------*/

const emit = defineEmits(['update:modelValue']);

/*--------------------------------------------------------------------------------------------------------------------*/

defineProps({
    modelValue: String,
    options: {
        type: Array,
        default: [],
    },
});

/*--------------------------------------------------------------------------------------------------------------------*/

const updateValue = (e) => {

    emit('update:modelValue', e.target.value);
};

/*--------------------------------------------------------------------------------------------------------------------*/
</script>

<template>

    <!-- *********************************************************************************************************** -->

    <div v-bind="$attrs">

        <div class="d-flex">

            <input type="text" :value="modelValue" @input="updateValue" />

        </div>

    </div>

    <!-- *********************************************************************************************************** -->

</template>

<style scoped>
/*--------------------------------------------------------------------------------------------------------------------*/

input {
    border: none;
    background-color: transparent;
}

/*--------------------------------------------------------------------------------------------------------------------*/
</style>
